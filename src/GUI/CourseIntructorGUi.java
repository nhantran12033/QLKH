    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
         int row = jTable1.getSelectedRow();
        TableModel model = jTable1.getModel();
        String id1 = model.getValueAt(row,0).toString();
        String id2 = model.getValueAt(row,1).toString();
        int i =0;
        int h=0;
        while(true){
            String id = jComboBox1.getItemAt(i).toString();
            if(id.equalsIgnoreCase(id1)){
                jComboBox1.setSelectedIndex(i);
                break;
            }
            i++;
        }
        while(true){
            String id3 = jComboBox2.getItemAt(h).toString();
            if(id3.equalsIgnoreCase(id2)){
                jComboBox2.setSelectedIndex(h);
                break;
            }
            h++;
        }
        
    }//GEN-LAST:event_jTable1MouseClicked


