    public void Load() {
       
        List<CourseIntructorDTO> listonl = spq.listonl();
        DefaultTableModel DFT = (DefaultTableModel) jTable1.getModel();
        DFT.setRowCount(0);
        for (CourseIntructorDTO sp : listonl) {
            String CourseID = sp.getCourseID();
            String url = sp.getPersonID();
            
            DFT.addRow(new Object[]{CourseID, url});
        }
     }
     private void xuLySua() {
        String CourseID = (String) jComboBox1.getSelectedItem();
        int vitri=-1;
        DefaultTableModel DFT = (DefaultTableModel) jTable1.getModel();
        DFT.setRowCount(0);
        for (CourseIntructorDTO nv : spq.TimKiem(CourseID)) {
            if(nv.getCourseID().equalsIgnoreCase(CourseID)){               
                vitri = 1;
            }
           
        }
        if(vitri < 0){
            JOptionPane.showMessageDialog(null, "Không tìm thấy ID");
            Load();
        }
        else{
           
       
            String url = (String) jComboBox2.getSelectedItem();
       
        
            boolean flag = spq.ChinhSua(CourseID,url);
            spq.docListonl();
            Load();
        
        }
        
        
    }
    public void Load3() {       
        String CourseID = jTextField2.getText();
        int vitri=-1;
        DefaultTableModel DFT = (DefaultTableModel) jTable1.getModel();
        DFT.setRowCount(0);
        for (CourseIntructorDTO nv : spq.TimKiem(CourseID)) {
            if(nv.getCourseID().equalsIgnoreCase(CourseID)){
                String course = nv.getCourseID();
                String url = nv.getPersonID();
                DFT.addRow(new Object[]{course, url});
                vitri = 1;
            }
           
        }
        if(vitri < 0){
            JOptionPane.showMessageDialog(null, "Không tìm thấy ID");
            spq.docListonl();
            Load();
        }
        else{
            JOptionPane.showMessageDialog(null, "Tìm thành công");
            
        }
        
       
        
    }
